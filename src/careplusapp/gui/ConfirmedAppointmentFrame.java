    private void showPatientInfo() {
        try{
            List <AppointmentPojo> appList = AppointmentDao.getConfirmAppointmentsByDoctorName(UserProfile.getUserName());
            if(appList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No data in DB","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            for(AppointmentPojo app:appList){
                Vector <String> v = new Vector <>();
                v.add(app.getPatientId());
                v.add(app.getPatientName());
                v.add(app.getOpd());
                v.add(app.getAppointmentDate());
                model.addRow(v);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }


